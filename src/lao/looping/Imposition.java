package lao.looping;

public class Imposition {





	public static void main(String[] args) {

		// for loop initialization; condition; increment/decrement

		/*
		 * for(int times=0; times<25; times++) {
		 * System.out.println("I can learn Program"); }
		 */
		/*
		 * int times=0;
		 * while (times<25) {
		 *  System.out.println("I can learn program");
		 * times++; 
		 * }
		 */

		int times=0;
		do {
			System.out.println("I can learn Program");
			times++;
		} while (times<25);

	}

}
